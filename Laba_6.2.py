dict = {"1": ["А", "В", "Е", "И", "Н", "О", "Р", "С", "Т"], "2": ["Д", "К", "Л", "М", "П", "У"],
        "3": ["Б", "Г", "Ё", "Ь", "Я"], "4": ["Й", "Ы"], "5": ["Ж", "З", "Х", "Ц", "Ч"],
        "8": ["Ш", "Э", "Ю"], "10": ["Ф", "Щ", "Ъ"]
}
C = 0

S = str(input("Введите слово: ")).upper()
LST = list(S)
for i in range(0, len(LST)):
    for j in dict.keys():
        Mass = dict[j]
        if str(LST[i]) in Mass:
            C = C + int(j)
            break

